using System;

namespace PrintNumbers
{
    class Program
    {
        static void Main(string[] args)
        {
            int i =1; // Initialize the counter variable

            // Loop while i is less than or equal to 100
            while (i <= 100)
            {
                // Print the current value of i
                Console.WriteLine(i);

                // Increment the counter variable
                i++;
            }
        }
    }
}
